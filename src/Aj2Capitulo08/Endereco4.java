package Aj2Capitulo08;

public class Endereco4 {
	private String rua;
	private int numero;
	
	public Endereco4(String rua, int numero) {
		this.rua = rua;
		this.numero = numero;
	}
	public String getRua() {
		return rua;
	}
	public void setRua(String rua) {
		this.rua = rua;
	}
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
}
